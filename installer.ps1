param (
    [string]$CSInstallerUrlOrActivationToken,
    [bool]$ForceReinstall = $false
)

$logPath = "$env:TEMP\CyberSmartActiveProtectInstaller.log"
Start-Transcript -Path $logPath

function Get-Desktop-Path {
    $programFilesPath = [Environment]::GetFolderPath("ProgramFiles")
    $programFilesx86Path = [Environment]::GetFolderPath("ProgramFilesX86")
    $cs64 = Join-Path -Path $programFilesx86Path -ChildPath "cs/CyberSmartActiveProtect-win32-x64"
    if (Test-Path -Path $cs64) {
        return $cs64
    }
    return Join-Path -Path $programFilesPath -ChildPath "cs/CyberSmartActiveProtect-win32-ia32"
}


function Check-if-Installed {
    $desktopAppPath = Get-Desktop-Path
    $daemonService = Get-Service -Name "CSDaemon" -ErrorAction SilentlyContinue
    $desktopAppExists = Test-Path -Path "$desktopAppPath\CyberSmartActiveProtect.exe"
    if ($daemonService -ne $null -and $desktopAppExists) {
        return $true
    }
    return $false
}

function Get-Installer-Url {
    $uuidRegex = "^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$"
    if ($CSInstallerUrlOrActivationToken -match "^https://") {
      write-host "Using provided installer URL"
        return $CSInstallerUrlOrActivationToken
    }
    if ($CSInstallerUrlOrActivationToken -match $uuidRegex) {
      write-host "Using provided activation token"
      return "https://cs-prod-windows-distribution.cybersmart.co.uk/build/$CSInstallerUrlOrActivationToken"
    }

    throw "Invalid argument. Please provide either an installer URL or an activation token"
}

function Download-And-Install {
  $tempFile = "$env:TEMP\CyberSmart Active Protect Installer.exe"
  try {
      $url = Get-Installer-Url
      write-host "Downloading file from $url to $tempFile"
      Invoke-WebRequest -Uri $url -OutFile $tempFile
      Write-Host "File downloaded successfully"
      Write-Host "Installing CyberSmart Active Protect"
      Start-Process -FilePath $tempFile -ArgumentList "--NoUI" -Wait
      Read-Installer-Logs
      write-host "Removing $tempFile"
      Remove-Item -Path $tempFile
  } catch {
      Write-Error "Failed to download file: $_"
  }
}

function Read-Installer-Logs {
    $logPath = "$env:TEMP\CSOnlineInstaller.log"
    if (Test-Path -Path $logPath) {
        Write-Host "Reading installer logs"
        $logContents = Get-Content -Path $logPath -Raw
        Write-Host $logContents
    }
}

if(Check-if-Installed){
  Write-Host "CyberSmart Active Protect is already installed"
  if($ForceReinstall){
      Write-Host "Forcing reinstallation"
      Download-And-Install
      Stop-Transcript
  }else{
      Stop-Transcript
      exit 0
  }
}else {
  Write-Host "CyberSmart Active Protect is not installed"
  Download-And-Install
  Stop-Transcript
}
